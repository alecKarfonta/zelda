void ConsoleLogo_Draw(ConsoleLogoState* this) {
    static s16 sTitleRotY = 0;
    static Lights1 sTitleLights = gdSPDefLights1(100, 100, 100, 255, 255, 255, 69, 69, 69);

    u16 idx;
    s32 pad1;
    Vec3f v3;
    Vec3f v1;
    Vec3f v2;
    s32 pad2;
    s32 pad3;
    int row, col, slice_idx, x, y;

    OPEN_DISPS(this->state.gfxCtx, "../z_title.c", 395);

    if (nintendo_rogo_static_hd_slices[0][0] != NULL) {
        // HD texture rendering - load slices in a 4x12 grid
        osSyncPrintf("HD TEXTURE LOADING: Using HD Nintendo logo texture slices\n");
        for (row = 0; row < 4; row++) {
            for (col = 0; col < 12; col++) {
                slice_idx = row * 12 + col;
                y = 94 + (row * 8);  // 8 pixels per row (4x scale)
                x = 97 + (col * 16); // 16 pixels per column (4x scale)
                osSyncPrintf("HD TEXTURE SLICE: Loading slice %d (row %d, col %d) at position (%d, %d)\n", 
                       slice_idx, row, col, x, y);
                gDPLoadTextureBlock(POLY_OPA_DISP++, nintendo_rogo_static_hd_slices[row][col], G_IM_FMT_I,
                                    G_IM_SIZ_8b, 64, 32, 0, G_TX_NOMIRROR | G_TX_WRAP, G_TX_NOMIRROR | G_TX_WRAP, G_TX_NOMASK,
                                    G_TX_NOMASK, G_TX_NOLOD, G_TX_NOLOD);
                gDPSetTileSize(POLY_OPA_DISP++, 1, this->uls, (this->ult & 0x7F) - (slice_idx << 2), 0, 0);
                gSPTextureRectangle(POLY_OPA_DISP++, x << 2, y << 2, (x + 64) << 2, (y + 32) << 2, G_TX_RENDERTILE, 0, 0, 1 << 10,
                                    1 << 10);
            }
        }
        osSyncPrintf("HD TEXTURE LOADING: Successfully loaded all 48 HD texture slices\n");
    }
} 